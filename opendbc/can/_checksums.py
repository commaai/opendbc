def honda_checksum(address: int, d: bytearray) -> int:
  s: int = 0
  extended: bool = address > 0x7FF
  addr: int = address
  while addr:
    s += addr & 0xF
    addr >>= 4
  d_len: int = len(d)
  for i in range(d_len):
    x: int = d[i]
    if i == d_len - 1:
      x >>= 4
    s += (x & 0xF) + (x >> 4)
  s = 8 - s
  if extended:
    s += 3
  return s & 0xF


def toyota_checksum(address: int, d: bytearray) -> int:
  s: int = len(d)
  addr: int = address
  while addr:
    s += addr & 0xFF
    addr >>= 8
  d_len_minus1: int = len(d) - 1
  for i in range(d_len_minus1):
    s += d[i]
  return s & 0xFF


def subaru_checksum(address: int, d: bytearray) -> int:
  s: int = 0
  addr: int = address
  while addr:
    s += addr & 0xFF
    addr >>= 8
  d_len: int = len(d)
  for i in range(1, d_len):
    s += d[i]
  return s & 0xFF


def chrysler_checksum(address: int, d: bytearray) -> int:
  checksum: int = 0xFF
  d_len_minus1: int = len(d) - 1
  for j in range(d_len_minus1):
    curr: int = d[j]
    shift: int = 0x80
    for _ in range(8):
      bit_sum: int = curr & shift
      temp_chk: int = checksum & 0x80
      if bit_sum:
        bit_sum = 0x1C
        if temp_chk:
          bit_sum = 1
        checksum = (checksum << 1) & 0xFF
        temp_chk = checksum | 1
        bit_sum ^= temp_chk
      else:
        if temp_chk:
          bit_sum = 0x1D
        checksum = (checksum << 1) & 0xFF
        bit_sum ^= checksum
      checksum = bit_sum & 0xFF
      shift >>= 1
  return (~checksum) & 0xFF


def fca_giorgio_checksum(address: int, d: bytearray) -> int:
  crc: int = 0
  d_len_minus1: int = len(d) - 1
  for i in range(d_len_minus1):
    crc ^= d[i]
    crc = CRC8J1850[crc]
  if address == 0xDE:
    return crc ^ 0x10
  elif address == 0x106:
    return crc ^ 0xF6
  elif address == 0x122:
    return crc ^ 0xF1
  else:
    return crc ^ 0x0A


def hkg_can_fd_checksum(address: int, d: bytearray) -> int:
  crc: int = 0
  d_len: int = len(d)
  for i in range(2, d_len):
    crc = ((crc << 8) ^ CRC16_XMODEM[(crc >> 8) ^ d[i]]) & 0xFFFF
  crc = ((crc << 8) ^ CRC16_XMODEM[(crc >> 8) ^ (address & 0xFF)]) & 0xFFFF
  crc = ((crc << 8) ^ CRC16_XMODEM[(crc >> 8) ^ ((address >> 8) & 0xFF)]) & 0xFFFF
  if d_len == 8:
    crc ^= 0x5F29
  elif d_len == 16:
    crc ^= 0x041D
  elif d_len == 24:
    crc ^= 0x819D
  elif d_len == 32:
    crc ^= 0x9F5B
  return crc


def volkswagen_mqb_meb_checksum(address: int, d: bytearray) -> int:
  crc: int = 0xFF
  d_len: int = len(d)
  for i in range(1, d_len):
    crc ^= d[i]
    crc = CRC8H2F[crc]
  counter: int = d[1] & 0x0F
  if address in VOLKSWAGEN_MQB_MEB_CONSTANTS:
    const: list[int] = VOLKSWAGEN_MQB_MEB_CONSTANTS[address]
    crc ^= const[counter]
    crc = CRC8H2F[crc]
  return crc ^ 0xFF


def xor_checksum(start_bit: int, d: bytearray, initial_value: int) -> int:
  checksum: int = initial_value
  checksum_byte: int = start_bit // 8
  d_len: int = len(d)
  for i in range(d_len):
    if i != checksum_byte:
      checksum ^= d[i]
  return checksum


def volkswagen_mlb_checksum(address: int, start_bit: int, d: bytearray) -> int:
  if address in VOLKSWAGEN_MLB_XOR_VALUES:
    return xor_checksum(start_bit, d, VOLKSWAGEN_MLB_XOR_VALUES[address])
  else:
    return volkswagen_mqb_meb_checksum(address, d)


def tesla_checksum(address: int, start_bit: int, d: bytearray) -> int:
  checksum: int = (address & 0xFF) + ((address >> 8) & 0xFF)
  checksum_byte: int = start_bit // 8
  d_len: int = len(d)
  for i in range(d_len):
    if i != checksum_byte:
      checksum += d[i]
  return checksum & 0xFF


def body_checksum(d: bytearray) -> int:
  crc: int = 0xFF
  for i in range(len(d) - 2, -1, -1):
    crc = CRC8BODY[crc ^ d[i]]
  return crc


def psa_checksum(address: int, start_bit: int, d: bytearray) -> int:
  chk_ini: int
  if address == 0x452:
    chk_ini = 0x4
  elif address == 0x38D:
    chk_ini = 0x7
  elif address == 0x42D:
    chk_ini = 0xC
  else:
    chk_ini = 0xB
  byte: int = start_bit // 8
  if start_bit % 8 >= 4:
    d[byte] &= 0x0F
  else:
    d[byte] &= 0xF0
  checksum: int = 0
  d_len: int = len(d)
  for i in range(d_len):
    b_val: int = d[i]
    checksum += (b_val >> 4) + (b_val & 0xF)
  return (chk_ini - checksum) & 0xF


def calc_checksum(sig_type: int, address: int, start_bit: int, d: bytearray) -> int:
  if sig_type == SignalType.HONDA_CHECKSUM:
    return honda_checksum(address, d)
  elif sig_type == SignalType.TOYOTA_CHECKSUM:
    return toyota_checksum(address, d)
  elif sig_type == SignalType.BODY_CHECKSUM:
    return body_checksum(d)
  elif sig_type == SignalType.VOLKSWAGEN_MQB_MEB_CHECKSUM:
    return volkswagen_mqb_meb_checksum(address, d)
  elif sig_type == SignalType.XOR_CHECKSUM:
    return xor_checksum(start_bit, d, 0)
  elif sig_type == SignalType.SUBARU_CHECKSUM:
    return subaru_checksum(address, d)
  elif sig_type == SignalType.CHRYSLER_CHECKSUM:
    return chrysler_checksum(address, d)
  elif sig_type == SignalType.HKG_CAN_FD_CHECKSUM:
    return hkg_can_fd_checksum(address, d)
  elif sig_type == SignalType.FCA_GIORGIO_CHECKSUM:
    return fca_giorgio_checksum(address, d)
  elif sig_type == SignalType.TESLA_CHECKSUM:
    return tesla_checksum(address, start_bit, d)
  elif sig_type == SignalType.PSA_CHECKSUM:
    return psa_checksum(address, start_bit, d)
  elif sig_type == SignalType.VOLKSWAGEN_MLB_CHECKSUM:
    return volkswagen_mlb_checksum(address, start_bit, d)
  return 0
