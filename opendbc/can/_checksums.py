from opendbc.can._types import SignalType


def _gen_crc8_table(poly: int) -> list[int]:
  table: list[int] = []
  for i in range(256):
    crc: int = i
    for _ in range(8):
      if crc & 0x80:
        crc = ((crc << 1) ^ poly) & 0xFF
      else:
        crc = (crc << 1) & 0xFF
    table.append(crc)
  return table


def _gen_crc16_table(poly: int) -> list[int]:
  table: list[int] = []
  for i in range(256):
    crc: int = i << 8
    for _ in range(8):
      if crc & 0x8000:
        crc = ((crc << 1) ^ poly) & 0xFFFF
      else:
        crc = (crc << 1) & 0xFFFF
    table.append(crc)
  return table


CRC8H2F: list[int] = _gen_crc8_table(0x2F)
CRC8J1850: list[int] = _gen_crc8_table(0x1D)
CRC8BODY: list[int] = _gen_crc8_table(0xD5)
CRC16_XMODEM: list[int] = _gen_crc16_table(0x1021)


